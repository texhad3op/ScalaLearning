  def run: Unit = {
    screen = new ScreenManager;
    try {
      var displayMode: DisplayMode = screen
        .findFirstCompatibleMode(POSSIBLE_MODES);
      screen.setFullScreen(displayMode);

      val window: Window = screen.getFullScreenWindow()
      window.setFont(new Font("Dialog", Font.PLAIN, 30))
      window.setBackground(Color.blue)
      window.setForeground(Color.white)

      window.setFocusTraversalKeysEnabled(false)

      window.addMouseListener(this)
      window.addMouseMotionListener(this)

      isRunning = true;
      loadImages
      animationLoop
    } finally {
      screen.restoreScreen
    }
  }

  def animationLoop: Unit = {
    var startTime: Long = System.currentTimeMillis
    var currTime: Long = startTime

    while (isRunning) {
      var elapsedTime: Long = System.currentTimeMillis - currTime;
      currTime += elapsedTime

      // draw and update screen
      val g: Graphics2D = screen.getGraphics
      draw(g)
      g.dispose
      screen.update
      Thread.sleep(2)

    }
  }

  def draw(g: Graphics): Unit = {

    g.fillRect(0, 0, screen.getWidth(), screen.getHeight())
    if (WIDTH - SCREEN_WIDTH > globalx && 0 < globalx)
      globalx += diffg;
    if (WIDTH - SCREEN_WIDTH == globalx)
      globalx = WIDTH - SCREEN_WIDTH - 1;
    if (0 == globalx)
      globalx = 1;

    for (fx <- globalx until globalx + SCREEN_WIDTH)
      for (fy <- 0 until 21) {
        if (1 == field(fy)(fx))
          g.drawImage(fon, (fx - globalx) * 32, fy * 27, null);
      }
    g.setColor(new Color(0, 0, 0));
    g.drawString("Живых осталось: " + army.length, 700, 600);

    if (cx < 550)
      diff = 1
    if (cx > 950)
      diff = -1

    cx = cx + diff
    g.drawRect((globalx * 5), 570, 160, 110)

    g.setColor(new Color(255, 0, 0))

    army.foreach(soldier => {
      g.fillOval(soldier.getX * 5, 576 + soldier.getY * 5, 3, 3)
      g.drawImage(soldier.getImage, (soldier.getX - globalx) * SCREEN_WIDTH,
        soldier.getY * SCREEN_HEIGHT, null);
    })

  }

