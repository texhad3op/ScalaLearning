    def getX: Int = x
    def getY: Int = y
    def getImage: Image
  }

  class Private(x: Int, y: Int) extends Human(x, y) { override def getImage: Image = privateImage }
  class Commander(x: Int, y: Int) extends Human(x, y) { override def getImage: Image = commanderImage }

